print("Integer typecasting into other data types")
a = 31
print("a = ", a , "type of a = ", type(a))

a = str(a)
print("a = ", a , "type of a = ", type(a))

a = float(a)
print("a = ", a , "type of a = ", type(a), end = "\n\n")

